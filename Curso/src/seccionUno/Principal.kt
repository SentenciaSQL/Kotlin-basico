package seccionUno

fun main() {
    // Enteros
    val entero = 30
    println(entero)

    val long = 123456789L
    println(long)

    val decimal = 3.1416
    println(decimal)

    val short = 1255.toShort()
    println(short)

    val byte = 127
    println(byte)

    // Flotantes
    val float = 3.14F
    println(float)

    // Caracteres
    val char = 'A'
    println(char)

    val charUnicode = '\u0041'
    println(charUnicode)

    val charDecimal: Char = 65.toChar()
    println(charDecimal)

    val charSimbolo = '$'
    println(charSimbolo)

    // Cadenas
    val cadena = "Hola Mundo"
    println(cadena)

    // Booleans
    val booleano = true
    println(booleano)

    val booleanoFalso: Boolean = false
    println(booleanoFalso)
}
